import numpy as np
x=np.array([[1,2],[3,4]])
print(x)
m=x.max(axis=0)
n=x.max(axis=1)
print(m,n)
o=x.min(axis=0)
p=x.min(axis=1)
print(o,p)
z=np.sqrt(x)
print(z)
median=np.median(x)
std=x.std()
mean=x.mean()
exp=np.exp(x)
print('median',median)
print('std',std)
print('mean',mean)
print('exp',exp)